export function celsiusToFahrenheit(celsius) { return celsius * 9/5 + 32; }
export function celsiusToKelvin(celsius) { return celsius + 273.15; }
export function fahrenheitToCelsius(fahrenehit) { return 5/9 * (fahrenehit - 32); }
export function fahrenheitToKelvin(fahrenehit) { return 5/9 * (fahrenehit - 32) + 273.15;}
export function kelvinToCelsius(kelvin) { return kelvin - 273.15; }
export function kelvinToFahrenheit(kelvin) { return 9/5 * (kelvin - 273.15) + 32; }